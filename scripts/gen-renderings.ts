const byDomain = {
  "interior-design": interiorDesign,
  lighting,
  flooring,
};

// Fail loudly if a curated range points at a missing image.
const allPaths = Object.values(byDomain)
  .flat()
  .flatMap((s) =>
    s.images ? s.images : (s.subsections ?? []).flatMap((sub) => sub.images),
  );
const missing = allPaths.filter((p) => !existsSync(`public${p}`));
if (missing.length) {
  console.error("Missing image files:\n" + missing.join("\n"));
  process.exit(1);
}

const out = `// AUTO-GENERATED by scripts/gen-renderings.ts — do not edit by hand.

export interface RenderingSubsection {
  title: string;
  images: string[];
}

export interface RenderingSet {
  title: string;
  width: number;
  height: number;
  images?: string[];
  subsections?: RenderingSubsection[];
}

export const renderingsByDomain: Record<string, RenderingSet[]> = ${JSON.stringify(
  byDomain,
  null,
  2,
)};
`;

writeFileSync("data/renderings.ts", out);
console.log(`wrote data/renderings.ts (${allPaths.length} images)`);
