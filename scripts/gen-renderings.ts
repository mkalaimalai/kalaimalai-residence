/**
 * Regenerate data/renderings.ts from the converted PDF page images in
 * public/images/renderings/<domain>/. Run after adding/replacing renderings:
 *   npx tsx scripts/gen-renderings.ts
 *
 * Images are produced from the source PDFs in artifacts/renderings/ via:
 *   pdftoppm -jpeg -jpegopt quality=82 -scale-to 1600 "<deck>.pdf" "<out>/<key>"
 *
 * The page→content mapping (cover / palette / room-divider slides) was read from
 * the source decks; section order and labels below are curated, not auto-derived.
 * Deck covers, room-title divider slides, and the "Thank you" slide are omitted —
 * each section gets its own heading instead.
 */
import { existsSync, writeFileSync } from "node:fs";

const PREFIX = "/images/renderings/interior-design";
const LIGHT_PREFIX = "/images/renderings/lighting";
const FLOOR_PREFIX = "/images/renderings/flooring";

/**
 * Build an inclusive `key-from..to` page-range helper bound to one domain's
 * image prefix. `pad` matches pdftoppm's zero-padding.
 */
const ranger =
  (prefix: string) =>
  (key: string, from: number, to: number, pad: number): string[] => {
    const out: string[] = [];
    for (let n = from; n <= to; n++) {
      out.push(`${prefix}/${key}-${String(n).padStart(pad, "0")}.jpg`);
    }
    return out;
  };

const range = ranger(PREFIX);
const lrange = ranger(LIGHT_PREFIX);
const frange = ranger(FLOOR_PREFIX);

interface RenderingSubsection {
  title: string;
  images: string[];
}
interface RenderingSet {
  title: string;
  width: number;
  height: number;
  images?: string[];
  subsections?: RenderingSubsection[];
}

